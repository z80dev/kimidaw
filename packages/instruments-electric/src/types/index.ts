/**
 * Electric - Electric Piano Physical Modeling Instrument
 * 
 * Based on Ableton Electric device - emulates Rhodes, Wurlitzer, and Pianet
 * electric pianos using physical modeling techniques.
 */

// ============================================================================
// MODEL TYPES
// ============================================================================

export type PianoModel = 'rhodes' | 'wurlitzer' | 'pianet';

// ============================================================================
// PICKUP SECTION
// ============================================================================

export type PickupType = 'fm' | 'em';

export interface PickupParams {
  /** Pickup symmetry/position (-1 to 1) */
  symmetry: number;
  /** Distance from tine/bar (0-1) */
  distance: number;
  /** Pickup type: FM (field coil) or EM (electromagnetic) */
  type: PickupType;
  /** Output level from pickup (0-1) */
  level: number;
}

// ============================================================================
// TINE / BAR SECTION
// ============================================================================

export interface TineParams {
  /** Tonal character of the tine/bar (0-1) */
  tone: number;
  /** Harmonic content/color (0-1) */
  color: number;
  /** Decay time of the tine vibration (0.01-5s) */
  decay: number;
  /** Output level (0-1) */
  level: number;
  /** Inharmonicity factor (0-1) */
  inharmonics: number;
}

// ============================================================================
// DAMPER SECTION
// ============================================================================

export interface DamperParams {
  /** Damper engagement amount (0-1) */
  amount: number;
  /** Damper tone character (0-1) */
  tone: number;
  /** Noise generated by damper (0-1) */
  noise: number;
}

// ============================================================================
// HAMMER SECTION
// ============================================================================

export interface HammerParams {
  /** Hammer hardness/stiffness (0-1) */
  hardness: number;
  /** Amount of mechanical noise (0-1) */
  noise: number;
  /** Force/velocity response curve (0-1) */
  force: number;
}

// ============================================================================
// AMP SECTION
// ============================================================================

export interface AmpParams {
  /** Preamp drive/saturation (0-1) */
  drive: number;
  /** Tone/EQ control (0-1) */
  tone: number;
  /** Output level (0-1) */
  level: number;
}

// ============================================================================
// TREMOLO EFFECT
// ============================================================================

export type TremoloWaveform = 'sine' | 'triangle' | 'saw' | 'square';

export interface TremoloParams {
  enabled: boolean;
  /** Rate in Hz (0.1-20) */
  rate: number;
  /** Modulation depth (0-1) */
  amount: number;
  /** LFO waveform */
  waveform: TremoloWaveform;
  /** Stereo phase offset (0-180 degrees) */
  stereoPhase: number;
  /** Sync to tempo */
  sync: boolean;
  /** Sync division if synced */
  syncDivision?: number;
}

// ============================================================================
// CHORUS EFFECT
// ============================================================================

export interface ChorusParams {
  enabled: boolean;
  /** Rate in Hz (0.1-10) */
  rate: number;
  /** Modulation depth (0-1) */
  amount: number;
  /** Number of delay lines (1-3) */
  voices: number;
}

// ============================================================================
// GLOBAL PARAMETERS
// ============================================================================

export interface GlobalParams {
  /** Global transpose in semitones (-24 to +24) */
  transpose: number;
  /** Fine tuning in cents (-100 to +100) */
  fineTune: number;
  /** Stretch tuning amount (0-1) */
  stretch: number;
  /** Number of voices for polyphony (1-32) */
  voices: number;
  /** Master volume (0-1) */
  volume: number;
  /** Velocity curve (0.1-5, 1=linear) */
  velocityCurve: number;
}

// ============================================================================
// MIDI MAPPINGS
// ============================================================================

export interface MidiMappingParams {
  /** Pitch bend range in semitones */
  pitchBendRange: number;
  /** Mod wheel controls tremolo depth */
  modToTremolo: boolean;
  /** Aftertouch amount (0-1) */
  aftertouchAmount: number;
  /** Sustain pedal enabled */
  sustainPedal: boolean;
}

// ============================================================================
// COMPLETE INSTRUMENT STATE
// ============================================================================

export interface ElectricState {
  /** Piano model type */
  model: PianoModel;
  /** Pickup characteristics */
  pickup: PickupParams;
  /** Tine/bar physics */
  tine: TineParams;
  /** Damper behavior */
  damper: DamperParams;
  /** Hammer characteristics */
  hammer: HammerParams;
  /** Amplifier section */
  amp: AmpParams;
  /** Tremolo effect */
  tremolo: TremoloParams;
  /** Chorus effect */
  chorus: ChorusParams;
  /** Global settings */
  global: GlobalParams;
  /** MIDI mappings */
  midi: MidiMappingParams;
}

