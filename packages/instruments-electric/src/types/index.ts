// ============================================================================
// VOICE EVENTS
// ============================================================================

export interface NoteEvent {
  type: 'note-on' | 'note-off';
  note: number;
  velocity: number;
  sampleOffset: number;
  channel?: number;
}

export interface ControlEvent {
  type: 'pitch-bend' | 'mod-wheel' | 'sustain' | 'aftertouch';
  value: number;
  sampleOffset: number;
}

export type ElectricEvent = NoteEvent | ControlEvent;
